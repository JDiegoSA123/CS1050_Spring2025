
public class M03L11LoopsConditions {

	
	
	public static void main(String[] args) {
		
	final int MAX = 10; 
	int sum;
		for (int x = 1; x < MAX; x++) 
		{
			
			sum = (x + x);
			
		}// end of for loop
		
		System.out.println("The sum of x is " + sum);
		
	}// end of main method

}// end of class
